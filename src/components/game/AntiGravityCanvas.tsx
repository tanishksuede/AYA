// Virtualized Frame Manager to fix the RAM issue
class FrameManager {
    private folder: string;
    private totalFrames: number;
    private cache: Map<number, HTMLImageElement> = new Map();
    private loading: Set<number> = new Set();
    private currentFrameIndex: number = -1;
    private bufferSizeAhead = 60; // Fetch 60 frames ahead (approx 480MB)
    private bufferSizeBehind = 30; // Keep 30 frames behind (approx 240MB)

    constructor(folder: string, totalFrames: number) {
        this.folder = folder;
        this.totalFrames = totalFrames;
    }

    public preloadInitial(count: number = 30, onProgress: (loaded: number) => void, onComplete: () => void) {
        let loaded = 0;
        const target = Math.min(count, this.totalFrames);
        for (let i = 0; i < target; i++) {
            this.loadFrame(i, () => {
                loaded++;
                onProgress(loaded);
                if (loaded === target) onComplete();
            });
        }
    }

    private loadFrame(index: number, onLoaded?: () => void) {
        if (this.cache.has(index) || this.loading.has(index)) {
            if (this.cache.has(index) && onLoaded) onLoaded();
            return;
        }

        this.loading.add(index);
        const img = new Image();
        img.src = `/assets/${this.folder}/ezgif-frame-${(index + 1).toString().padStart(3, '0')}.jpg`;
        img.onload = () => {
            this.cache.set(index, img);
            this.loading.delete(index);
            if (onLoaded) onLoaded();
        };
        img.onerror = () => {
            this.loading.delete(index);
            if (onLoaded) onLoaded();
        };
    }

    public updateTargetFrame(index: number) {
        if (this.currentFrameIndex === index) return;
        this.currentFrameIndex = index;

        // Prefetch nearby frames aggressively
        for (let i = index - 10; i <= index + this.bufferSizeAhead; i++) {
            if (i >= 0 && i < this.totalFrames) {
                this.loadFrame(i);
            }
        }

        // Evict old frames to free RAM (only outside our safe window)
        for (const [key] of this.cache.entries()) {
            if (key < index - this.bufferSizeBehind || key > index + this.bufferSizeAhead + 10) {
                this.cache.delete(key);
            }
        }
    }

    public getFrame(index: number): HTMLImageElement | undefined {
        return this.cache.get(index);
    }
    
    public cleanup() {
        this.cache.clear();
        this.loading.clear();
    }
}

interface AntiGravityCanvasProps {
    progress: MotionValue<number>;
    onReady: () => void;
}

export function AntiGravityCanvas({ progress, onReady }: AntiGravityCanvasProps) {
    const isCandyMode = useUserStore((state) => state.isCandyMode);
    const setIntroVideoCompleted = useUserStore((state) => state.setIntroVideoCompleted);
    const totalFrames = isCandyMode ? 40 : 240;
    const folder = isCandyMode ? 'map_frames' : 'map_frames_dark';

    const canvasRef = useRef<HTMLCanvasElement>(null);
    const frameManager = useRef<FrameManager | null>(null);
    const [imagesLoaded, setImagesLoaded] = useState(0);
    const [isReady, setIsReady] = useState(false);
    
    // We only track progress to draw
    const lastDrawnIndexRef = useRef<number>(-1);
    const loopRef = useRef<number>(0);

    useEffect(() => {
        setIntroVideoCompleted(false);
        setImagesLoaded(0);

        if (frameManager.current) {
            frameManager.current.cleanup();
        }

        const manager = new FrameManager(folder, totalFrames);
        frameManager.current = manager;

        manager.preloadInitial(
            30, 
            (loaded) => setImagesLoaded(loaded),
            () => {
                setIsReady(true);
                setIntroVideoCompleted(true);
                onReady();
            }
        );

        return () => {
            if (frameManager.current) frameManager.current.cleanup();
        };
    }, [folder, totalFrames, setIntroVideoCompleted, onReady]);

    useEffect(() => {
        if (!isReady || !canvasRef.current || !frameManager.current) return;

        const canvas = canvasRef.current;
        const ctx = canvas.getContext('2d', { alpha: false });
        if (!ctx) return;

        let canvasWidth = window.innerWidth;
        let canvasHeight = window.innerHeight;
        let dpr = window.devicePixelRatio || 1;

        const setupCanvasContext = () => {
            canvasWidth = window.innerWidth;
            canvasHeight = window.innerHeight;
            dpr = window.devicePixelRatio || 1;

            canvas.width = canvasWidth * dpr;
            canvas.height = canvasHeight * dpr;
            canvas.style.width = `${canvasWidth}px`;
            canvas.style.height = `${canvasHeight}px`;

            ctx.imageSmoothingEnabled = true;
            ctx.imageSmoothingQuality = 'high';
        };

        setupCanvasContext();

        const renderNativeFrame = (index: number) => {
            const safeIndex = Math.max(0, Math.min(totalFrames - 1, index));
            
            frameManager.current?.updateTargetFrame(safeIndex);
            
            const img = frameManager.current?.getFrame(safeIndex);
            if (!img || !img.complete) return; // Wait until loaded

            const canvasRatio = (canvasWidth * dpr) / (canvasHeight * dpr);
            const imgRatio = img.width / img.height;

            let drawWidth = canvasWidth * dpr;
            let drawHeight = canvasHeight * dpr;
            let offsetX = 0;
            let offsetY = 0;

            if (canvasRatio > imgRatio) {
                drawHeight = (canvasWidth * dpr) / imgRatio;
                offsetY = ((canvasHeight * dpr) - drawHeight) / 2;
            } else {
                drawWidth = (canvasHeight * dpr) * imgRatio;
                offsetX = ((canvasWidth * dpr) - drawWidth) / 2;
            }

            ctx.drawImage(img, offsetX, offsetY, drawWidth, drawHeight);
        };

        const handleResize = () => {
            setupCanvasContext();
            lastDrawnIndexRef.current = -1; // Force redraw
        };
        window.addEventListener('resize', handleResize);

        const tick = () => {
            const currentProgress = progress.get() || 0; 
            const index = Math.floor(currentProgress * (totalFrames - 1));
            
            // Always ask FrameManager to fetch around the target
            frameManager.current?.updateTargetFrame(Math.max(0, Math.min(totalFrames - 1, index)));

            // Only draw if the target frame index changed
            if (index !== lastDrawnIndexRef.current) {
                // Try to find the closest loaded frame to prevent visual freezing
                let bestImg = null;
                let bestIndex = -1;

                // Search outwards from target index to find the nearest loaded frame
                for (let offset = 0; offset <= 20; offset++) {
                    // Try exact or ahead
                    let checkIdx = Math.max(0, Math.min(totalFrames - 1, index + offset));
                    let img = frameManager.current?.getFrame(checkIdx);
                    if (img && img.complete) {
                        bestImg = img;
                        bestIndex = checkIdx;
                        break;
                    }
                    
                    // Try behind
                    if (offset > 0) {
                        checkIdx = Math.max(0, Math.min(totalFrames - 1, index - offset));
                        img = frameManager.current?.getFrame(checkIdx);
                        if (img && img.complete) {
                            bestImg = img;
                            bestIndex = checkIdx;
                            break;
                        }
                    }
                }

                if (bestImg && bestIndex !== lastDrawnIndexRef.current) {
                    renderNativeFrame(bestIndex);
                    lastDrawnIndexRef.current = bestIndex;
                }
            }

            loopRef.current = requestAnimationFrame(tick);
        };

        // Initial draw setup
        const initialProgress = progress.get() || 0;
        const initialIndex = Math.floor(initialProgress * (totalFrames - 1));
        renderNativeFrame(initialIndex);
        lastDrawnIndexRef.current = initialIndex;
        tick();

        return () => {
            window.removeEventListener('resize', handleResize);
            cancelAnimationFrame(loopRef.current);
        };
    }, [isReady, progress, totalFrames]);

    return (
        <>
            {!isReady && createPortal(
                <div className="fixed inset-0 z-[9999] flex flex-col items-center justify-center bg-[#050814]">
                    <div className="absolute bottom-1/2 translate-y-1/2 flex flex-col items-center text-cyan-400 font-mono tracking-widest font-bold z-10 w-64 max-w-[80%]">
                        <div className="text-sm mb-4 animate-pulse">INITIATING TIMELINE...</div>
                        <div className="w-full h-1.5 bg-slate-800 rounded-full overflow-hidden shadow-[0_0_15px_rgba(0,241,254,0.3)]">
                            <div
                                className="h-full bg-cyan-400 transition-all duration-200"
                                style={{ width: `${(imagesLoaded / 30) * 100}%` }}
                            />
                        </div>
                    </div>
                </div>,
                document.body
            )}
            <canvas
                ref={canvasRef}
                className="fixed top-0 left-0 w-full h-full pointer-events-none z-0"
            />
        </>
    );
}
